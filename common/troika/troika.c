void PrintTroikaSlice(trit_t *state, int slice) {
  fprintf(stderr, "#### Slice %i ####\n", slice);
  for (int row = 0; row < ROWS; ++row) {
    for (int column = 0; column < COLUMNS; ++column) {
      fprintf(stderr, "%i ", state[slice * SLICES + row * COLUMNS + column]);
    }
    fprintf(stderr, "\n");
  }
  fprintf(stderr, "------------------\n");
  for (int i = 0; i < COLUMNS; i++) {
    fprintf(stderr, "%i ",
            (state[slice * SLICES + 0 * COLUMNS + i] +
             state[slice * SLICES + 1 * COLUMNS + i] +
             state[slice * SLICES + 2 * COLUMNS + i]) %
                3);
  }
  fprintf(stderr, "\n");
}

